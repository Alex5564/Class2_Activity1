import math 

angle_degree = 45
angle_radians = math.radians(angle_degree)

sine_value = math.sin(angle_radians)
cosine_value = math.cos(angle_radians)
tangent_value = math.tan(angle_radians)

print(f"Sine of {angle_degree} degrees: {sine_value}")
print(f"Cosine of {angle_degree} degrees: {cosine_value}")
print(f"Tangent of {angle_degree} degrees: {tangent_value}")
